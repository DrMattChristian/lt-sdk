void TestPublicKey(Tilt *tilt) {
    LTDriverCryptoKeyManager *keyManager = lt_createobject(LTDriverCryptoKeyManager);
    LTDriverCryptoCertManager *certManager = lt_createobject(LTDriverCryptoCertManager);
    TILT_EXPECT_TRUE(tilt, certManager->API->SetCertificate("ROKUCA", s_RokuCa, sizeof(s_RokuCa), false, true), "Set ROKUCA cert fail");
    TILT_EXPECT_TRUE(tilt, certManager->API->SetCertificate("comm", (u8 *)&s_ClientCertificate_ecdsa, sizeof(s_ClientCertificate_ecdsa), true, true), "Set comm cert fail");

    LTPublicKey publicKey;
    lt_memset(&publicKey, 0, sizeof(LTPublicKey));
    TILT_INFO(tilt, "Test public key");
    TILT_EXPECT_TRUE(tilt, keyManager->API->SetPublicKey("comm", &s_ClientPublicKey_ecdsa, false), "Set comm key fail");
    TILT_EXPECT_TRUE(tilt, keyManager->API->GetPublicKey("comm", &publicKey), "Get comm key fail");
    TILT_EXPECT_TRUE(tilt, s_ClientPublicKey_ecdsa.type == publicKey.type, "comm key type mismatch");
    TILT_EXPECT_TRUE(tilt, s_ClientPublicKey_ecdsa.keyLen == publicKey.keyLen, "comm key len mismatch");
    TILT_EXPECT_TRUE(tilt, s_ClientPublicKey_ecdsa.keyIdLen == publicKey.keyIdLen, "Comm key id len mismatch");
    TILT_EXPECT_TRUE(tilt, lt_memcmp(s_ClientPublicKey_ecdsa.key, publicKey.key, publicKey.keyLen) == 0, "comm key mismatch");
    TILT_EXPECT_TRUE(tilt, lt_memcmp(s_ClientPublicKey_ecdsa.keyId, publicKey.keyId, publicKey.keyIdLen) == 0, "comm key id mismatch");
    keyManager->API->FreePublicKey(&publicKey);

    TILT_INFO(tilt, "Test CA key");
    TILT_EXPECT_TRUE(tilt, keyManager->API->SetCaPublicKey("ROKUCA", &s_RokuCaKey, false), "Set CA key fail");
    TILT_EXPECT_TRUE(tilt, keyManager->API->GetCaPublicKey("ROKUCA", &publicKey), "Get CA key fail");
    TILT_EXPECT_TRUE(tilt, s_RokuCaKey.type == publicKey.type, "CA key type mismatch");
    TILT_EXPECT_TRUE(tilt, s_RokuCaKey.keyLen == publicKey.keyLen, "CA key len mismatch");
    TILT_EXPECT_TRUE(tilt, s_RokuCaKey.keyIdLen == publicKey.keyIdLen, "CA key id len mismatch");
    TILT_EXPECT_TRUE(tilt, lt_memcmp(s_RokuCaKey.key, publicKey.key, publicKey.keyLen) == 0, "CA key mismatch");
    TILT_EXPECT_TRUE(tilt, lt_memcmp(s_RokuCaKey.keyId, publicKey.keyId, publicKey.keyIdLen) == 0, "CA key id mismatch");
    keyManager->API->FreePublicKey(&publicKey);

    lt_destroyobject(certManager);
    lt_destroyobject(keyManager);
}

