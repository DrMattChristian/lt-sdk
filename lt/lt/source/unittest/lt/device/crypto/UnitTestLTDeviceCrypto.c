void TestSecureCryptos(Tilt *tilt) {
    // Secure key manager
    LTSecureKeyManager *secKeyManager = lt_createobject(LTSecureKeyManager);
    if (secKeyManager) {
        TILT_INFO(tilt, "Test secure key manager");
        LTDriverCrypto_KeyReference *reference = secKeyManager->API->SetKey(k78, AES128_KEY_LENGTH, kLTDriverCrypto_KeyType_Key128, kLTDriverCrypto_SecureKeyType_Wrapped);
        if (reference) {
            PrintBytes(tilt, reference->referenceBuf, 0, reference->referenceLen);
            // Secure CBC
            TILT_INFO(tilt, "Test secure Aes128Cbc");
            LTSecureCryptoAes128Cbc *aescbc = lt_createobject(LTSecureCryptoAes128Cbc);
            if (aescbc) {
                LTSystemCryptoResult result = aescbc->API->Encrypt(reference, iv78, p78, sizeof(p78), s_output);
                TILT_EXPECT_TRUE(tilt, (kLTSystemCrypto_Result_Ok == result) && (lt_memcmp(s_output, ec78, sizeof(ec78)) == 0), "Encrypt error");
                result = aescbc->API->Decrypt(reference, iv78, ec78, sizeof(ec78), s_output);
                TILT_EXPECT_TRUE(tilt, (kLTSystemCrypto_Result_Ok == result) && (lt_memcmp(s_output, p78, sizeof(p78)) == 0), "Decrypt error");
                secKeyManager->API->FreeKeyReference(reference);
                lt_destroyobject(secKeyManager);
            } else {
                TILT_WARNING(tilt, "No support of secure aes128-cbc !!!");
            }
        } else {
            TILT_WARNING(tilt, "No support of wrap crypto !!!");
        }
    } else {
        TILT_WARNING(tilt, "No support of secure key manager !!!");
    }

    LTDeviceIdentity *identity = lt_openlibrary(LTDeviceIdentity);
    u8 key[AES128_KEY_LENGTH];
    if (identity->GetAESKey(key, 1)) {
        LTDriverCrypto_KeyReference keyReference;
        keyReference.secureKeyType = kLTDriverCrypto_SecureKeyType_Provision;
        LTSystemCryptoResult result = kLTSystemCrypto_Result_Ok;

        TILT_INFO(tilt, "Test secure Aes128Cbc");
        LTSecureCryptoAes128Cbc *aescbc = lt_createobject(LTSecureCryptoAes128Cbc);
        if (aescbc) {
            keyReference.keyType = kLTDriverCrypto_KeyType_Key128;
            keyReference.provisionId = kLTDriverCrypto_ProvisionId_AesKey1;
            result = aescbc->API->Encrypt(&keyReference, iv78, p78, sizeof(p78), s_output);
            if (kLTSystemCrypto_Result_NoSupport == result) {
                TILT_WARNING(tilt, "No support of secure aes128-cbc on provisioned aes keys !!!");
            } else {
                TILT_EXPECT_TRUE(tilt, (kLTSystemCrypto_Result_Ok == result), "encrypt fail");
                result = aescbc->API->Decrypt(&keyReference, iv78, s_output, sizeof(p78), s_output);
                TILT_EXPECT_TRUE(tilt, (kLTSystemCrypto_Result_Ok == result) && (lt_memcmp(s_output, p78, sizeof(p78)) == 0), "decrypt fail");
            }
            lt_destroyobject(secKeyManager);
        } else {
            TILT_WARNING(tilt, "No support of secure aes128-cbc !!!");
        }

        TILT_INFO(tilt, "Test secure HmacSha256");
        LTSecureCryptoHmacSha256 *secHmac = lt_createobject(LTSecureCryptoHmacSha256);
        if (secHmac) {
            keyReference.keyType = kLTDriverCrypto_KeyType_Key128;
            keyReference.provisionId = kLTDriverCrypto_ProvisionId_AesKey1;
            result = secHmac->API->GenHmac(&keyReference, (u8 *)"key1", 4, s_output);
            TILT_EXPECT_TRUE(tilt, kLTSystemCrypto_Result_Ok == result, "hmac key1 fail %d", result);
            keyReference.provisionId = kLTDriverCrypto_ProvisionId_AesKey2;
            result = secHmac->API->GenHmac(&keyReference, (u8 *)"key2", 4, s_output);
            TILT_EXPECT_TRUE(tilt, kLTSystemCrypto_Result_Ok == result, "hmac key2 fail %d", result);
            lt_destroyobject(secHmac);
        } else {
            TILT_WARNING(tilt, "No support of secure hmac-sha256 !!!");
        }

        TILT_INFO(tilt, "Test secure Sha256");
        LTSecureCryptoSha256 *secSha = lt_createobject(LTSecureCryptoSha256);
        if (secSha) {
            keyReference.provisionId = kLTDriverCrypto_ProvisionId_AesKey1;
            result = secSha->API->GenDigest(&keyReference, (u8 *)"key1", 4, s_output);
            TILT_EXPECT_TRUE(tilt, kLTSystemCrypto_Result_Ok == result, "sha key1 fail %d", result);
            keyReference.provisionId = kLTDriverCrypto_ProvisionId_AesKey2;
            result = secSha->API->GenDigest(&keyReference, (u8 *)"key2", 4, s_output);
            TILT_EXPECT_TRUE(tilt, kLTSystemCrypto_Result_Ok == result, "sha key2 fail %d", result);
            lt_destroyobject(secSha);
        } else {
            TILT_WARNING(tilt, "No support of secure sha256 !!!");
        }

        TILT_INFO(tilt, "Test secure EcdsaP256");
        LTSecureCryptoEcdsaP256 *secEcdsa = lt_createobject(LTSecureCryptoEcdsaP256);
        if (secEcdsa) {
            const u8 data[] = "data";
            keyReference.keyType = kLTDriverCrypto_KeyType_P256;
            keyReference.provisionId = kLTDriverCrypto_ProvisionId_CommPrivate;
            secEcdsa->API->Sign(&keyReference, data, sizeof(data), s_output, s_output + ECDSA_P256_SIGNATURE_LENGTH);
            LTSystemCryptoResult ret = secEcdsa->API->Verify(data, sizeof(data), s_output, s_output + ECDSA_P256_SIGNATURE_LENGTH);
            TILT_EXPECT_TRUE(tilt, kLTSystemCrypto_Result_Ok == ret, "wrong signature %d", ret);
            lt_destroyobject(secEcdsa);
        } else {
            TILT_WARNING(tilt, "No support of secure ecdsaP256 !!!");
        }

    } else {
        TILT_WARNING(tilt, "No key to test !!!");
    }
    lt_closelibrary(identity);
}

